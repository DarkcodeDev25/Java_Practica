public class postIncremento_postDecremento {
    public static void main(String[] args){
        int c=0;
        int d=0;
        c++;
        c++;
        c++;
        c++;
        c++;
        System.out.println(c);
        
        d--;
        d--;
        d--;
        d--;
        d--;
        System.out.println(d);
        
        ++c;
        c--;
        System.out.println(c);
        System.out.println(c++);
    }
    
}
